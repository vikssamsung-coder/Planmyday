r"""
workspace.py — first-launch bootstrap.

Called once at app start. Idempotent: safe to run every launch; it only creates
what's missing and never overwrites real data.

On a fresh machine it builds, under D:\\Sarthi - Plan My Day\\ (see paths.py):

  Sarthi - Plan My Day\\
    README.md                         <- md: what this folder is
    _common\\
      users_master.xlsx               <- xlsx: logins
      monthly_targets.xlsx            <- xlsx
      learning_bank.xlsx              <- xlsx
      role_prompts\\
        sales_rm.md, trainer.md, ...  <- md: the role coaching prompts
    <user_key>\\
      monthly_plan.xlsx               <- xlsx
      monthly_achievement.xlsx        <- xlsx
      tasks.xlsx                      <- xlsx
      day_updates.xlsx                <- xlsx
      briefs\\                         <- md daily briefs land here
      reports\\                        <- docx + xlsx reports land here

The Word/md daily artifacts themselves are generated by report.py as the app
runs; this module guarantees the folders and the seed/reference files exist.
"""

